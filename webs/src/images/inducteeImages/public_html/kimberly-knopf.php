            Kimberly “Kim” Brown Knopf is founder, president, and CEO of
            Innovative Mattress Solutions (iMS), a Lexington, Kentucky-based
            mattress retailer operating as Sleep Outfitters, Mattress Warehouse
            and Mattress King. With a regional footprint spanning six
            states—Kentucky, West Virginia, Ohio, Tennessee, Alabama, and
            Indiana—the company has grown from humble beginnings as a single
            store in South Charleston, West Virginia, to more than 160 mattress
            showrooms through Knopf’s stewardship and vision. “Our success over
            three-plus decades has been grounded on a unified and common belief
            that we truly make the world better by helping people sleep better,”
            Knopf said. “Our people, products, and partners must always be
            aligned to deliver an exceptional customer experience.”

            A native of Louisville, Ky., Knopf graduated from the University of
            Kentucky in 1982. In 1983, she started her own business, Mattress
            Warehouse. Knopf worked tirelessly in every aspect of her business
            from the store-level up, from buying merchandise to advertising,
            from selling to delivery. She grew her business across West
            Virginia before venturing into Kentucky in 2002, where the Sleep
            Outfitters brand was born. Growth came quickly, as a series of
            acquisitions doubled the company’s size over the next two years.
            2012 saw the acquisition of Alabama retailer Mattress King,
            expanding their reach to six states.

