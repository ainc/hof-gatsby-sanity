            Before retiring as CEO in 2010, he designed the company’s recurring
            revenue business model, which amounted to an excess of $50 million.
            Cobb remains on the company’s board today.

            Currently, Cobb serves as Entrepreneur in Residence with Chrysalis
            Ventures and on the boards of several companies, including
            CompleteSet LLC and EDJ Analytics. He is a three-time Inc. 500 CEO
            and a three-time Kentucky Entrepreneur of the Year.

